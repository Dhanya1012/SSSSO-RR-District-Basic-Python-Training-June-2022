#Arithmetic Operations in Python

num1 = int(input("Enter a value: "))
num2 = int (input("Enter b value: "))
add = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2
exp = num1 ** num2 #pow(num1,num2)
print("Addition: ", add);
print("Subtraction: ", sub);
print("Multiplication: ", mul);
print("Division: ", div);
print("Exponent: ", exp);