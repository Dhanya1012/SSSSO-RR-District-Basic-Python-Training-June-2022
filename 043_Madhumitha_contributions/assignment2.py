#Write a program to find the maximum, minimum, average,
#addition, multiplication of any 2 integer numbers

a = 29
b = 44
  
maximum = max(a, b)
print("the maximum of given numbers is:",maximum)

minimum = min(a,b)
print("the minimum of given numbers is:",minimum)

avg = (a+b)/2
print("the average of given numbers is:",avg)

add = a+b
print("the addition of given numbers is:",add)


mul = a*b
print("the multiplication of given numbers is:",mul)

